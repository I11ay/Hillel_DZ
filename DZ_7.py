# Третье задание:
# Программа с помощью библиотеки random генерирует случайное число от 1 до 10, задача пользователя угадать это число за 3 попытки. В случае если пользователь указал больше загаданного числа, то нужно вывести "Бери меньше" и наоборот.
rand_numb = random.randint(1, 10)


for b in range(3):
    b += 1
    i = int(input(f"Попытка #{b}: "))
    if i == rand_numb:
        print('Ты угадал!')
        break
    elif i > rand_numb:
        print('Бери меньше')
    elif i < rand_numb:
        print('Бери больше')

